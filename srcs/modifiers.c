void	flags(const char *format, t_struct *f)
{
	while (ft_strchr("-0", format[f->i]))
	{
		if (format[f->i] == '-')
			f->minus = 1;
		if (format[f->i] == '0')
			f->zero = 1;
		f->i++;
	}
}

